package com.util;

import java.util.Collection;
import java.util.LinkedHashMap;
import java.util.Map;
import java.util.Set;

/**
 * 此类定义的是数据工厂类MM，用于存储请求传递过来的所有信息 将该信息作为公共信息方便后台程序读取.该类继承LinkedHashMap
 * 类并重写其父类的方法，其实MM类的实质还是LinkedHashMap类。
 * 
 * @author sqy
 * 
 */
public class MM extends LinkedHashMap {
	/*
	 * LinkedHashMap 是HashMap的一个子类，保存了记录的插入顺序，
	 * 因为LinkedHashMap的遍历速度只和实际数据有关，和容量无关， 而HashMap的遍历速度和他的容量有关。
	 */
	/**
	 * 返回此映射中的键-值映射关系数。
	 */
	@Override
	public int size() {
		return super.size();
	}

	/**
	 * 如果此映射不包含键-值映射关系，则返回 true
	 */
	@Override
	public boolean isEmpty() {
		return super.isEmpty();
	}

	/**
	 * 如果此映射包含对于指定键的映射关系，则返回 true
	 */
	@Override
	public boolean containsKey(Object key) {
		return super.containsKey(key);
	}

	/**
	 * 如果此映射将一个或多个键映射到指定值，则返回 true。
	 */
	@Override
	public boolean containsValue(Object value) {
		return super.containsValue(value);
	}

	/**
	 * 返回指定键所映射的值；如果对于该键来说，此映射不包含任何映射关系，则返回 null
	 */
	@Override
	public Object get(Object key) {
		return super.get(key);
	}

	/**
	 * 在此映射中关联指定值与指定键。
	 */
	@Override
	public Object put(Object key, Object value) {
		return super.put(key, value);
	}

	/**
	 * 从此映射中移除指定键的映射关系（如果存在）。
	 */
	@Override
	public Object remove(Object key) {
		return super.remove(key);
	}

	/**
	 * 将指定映射的所有映射关系复制到此映射中， 这些映射关系将替换此映射目前针对指定映射中所有键的所有映射关系。
	 */
	@Override
	public void putAll(Map m) {
		super.putAll(m);
	}

	/**
	 * 从该映射中移除所有映射关系。
	 */
	@Override
	public void clear() {
		super.clear();
	}

	/**
	 * 返回此映射中所包含的键的 Set 视图。
	 */
	@Override
	public Set keySet() {

		return super.keySet();
	}

	/**
	 * 返回此映射所包含的值的 Collection 视图。
	 */
	@Override
	public Collection values() {
		return super.values();
	}

	/**
	 * 返回此映射所包含的映射关系的 Set 视图。
	 */
	@Override
	public Set entrySet() {
		return super.entrySet();
	}
}
